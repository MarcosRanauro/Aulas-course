function adicao(a, b){
    return a + b;
}
console.log(adicao(5, 5));

function subtracao(a, b){
    return a - b;
}
console.log(subtracao(5, 5));

function multiplicacao(a, b){
    return a * b;
}
console.log(multiplicacao(5, 5));

function divisao(a, b){
    return a / b;
}
console.log(divisao(5, 5));

function resto(a, b){
    return a % b;
}
console.log(resto(5, 5));